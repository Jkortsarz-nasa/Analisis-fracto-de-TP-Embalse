def add_converted_units_imperial_tables(df: pd.DataFrame) -> pd.DataFrame:
    """
    Agrega columnas SI a tablas con columnas imperiales:
    - DISTANCE (INCHES) -> Distancia desde entrada F/C (m)
    - PRESSURE (PSI)    -> Presión (MPa(a))
    - STRESS 1 (PSI)    -> Esfuerzo 1 (MPa)
    - STRESS 2 (PSI)    -> Esfuerzo 2 (MPa)
    - TEMPERATURE (°F)  -> Temperatura (°C)
    """
    psi_to_mpa = 0.006894757293168361
    inch_to_m = 0.0254

    df = df.copy()

    if "DISTANCE (INCHES)" in df.columns:
        df["Distancia desde entrada F/C (m)"] = (df["DISTANCE (INCHES)"] * inch_to_m)

    if "PRESSURE (PSI)" in df.columns:
        df["Presión (MPa(a))"] = (df["PRESSURE (PSI)"] * psi_to_mpa)

    if "STRESS 1 (PSI)" in df.columns:
        df["Esfuerzo 1 (MPa)"] = (df["STRESS 1 (PSI)"] * psi_to_mpa)

    if "STRESS 2 (PSI)" in df.columns:
        df["Esfuerzo 2 (MPa)"] = (df["STRESS 2 (PSI)"] * psi_to_mpa)

    if "TEMPERATURE (°F)" in df.columns:
        df["Temperatura (°C)"] = ((df["TEMPERATURE (°F)"] - 32.0) * (5.0 / 9.0))

    return df



